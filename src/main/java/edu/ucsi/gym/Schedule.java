    private void refresh(){
        con = SQLConnection.ConnectorDb();
        try{
            DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
            tblModel.setRowCount(0);
            Statement st = con.createStatement();
            String sql = "Select bookingID, trainerName, bookingDate, bookingStatus from gymapp.bookings Where userEmail ='"+em+"';";
            rs = st.executeQuery(sql);
            System.out.println(sql);

            while(rs.next()){
                String ID = String.valueOf(rs.getInt("bookingID"));
                String name = String.valueOf(rs.getString("trainerName"));
                String date = String.valueOf(rs.getDate("bookingDate"));
                String status = String.valueOf(rs.getString("bookingStatus"));
                Status = String.valueOf(rs.getString("bookingStatus"));

                if(status.equals("A")){
                    status = "Active";
                }else{
                    status = "Canceled";
                }

                String tbData[] = {ID,date,status,name};
                tblModel.addRow(tbData);
            }
            con.close();
        }catch(Exception e){
            System.out.println(e);
        }
    }
