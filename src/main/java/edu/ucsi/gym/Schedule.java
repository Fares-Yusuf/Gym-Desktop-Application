    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        Date date;
        date = sessionDate.getDate();

        if(trainerNames.getSelectedIndex() == 0){
                JOptionPane.showMessageDialog(this, "Please Choose Trainer");
                return;
        }
        else if(date == null){
            JOptionPane.showMessageDialog(this, "Missing Date");
            return;
        }
        else{
            try{
                SimpleDateFormat Date_Format = new SimpleDateFormat("yyyy-MM-dd");
                String date1=""+Date_Format.format(sessionDate.getDate());
                con = SQLConnection.ConnectorDb();
                String sql= "Update gymapp.bookings SET trainerName = '"+trainerNames.getSelectedItem()+"', bookingDate = '"+date1+"' WHERE bookingID ="+id+";";
                System.out.println(sql);
                pst = con.prepareStatement(sql);
                System.out.println(pst);
                pst.execute();                
                JOptionPane.showMessageDialog(null, "Booking Updated!");
                con.close();
            }
            catch(Exception ex){
                JOptionPane.showMessageDialog(this, ex);
            }
            refresh();
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

